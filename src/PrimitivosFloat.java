public class PrimitivosFloat {
    public static void main(String[] args) {
        
        float realFloatOne = 1;
        System.out.println("realFloat = " + realFloatOne);
        
        float realFloatTwo = 2.0f;
        System.out.println("realFloatTwo = " + realFloatTwo);

        float realFloatThree = 2.12e3f; // 2120f;
        System.out.println("realFloatThree = " + realFloatThree);

        float realFloatFour = 1.5e-3f; // 0.0015f;
        System.out.println("realFloatThree = " + realFloatFour);

        float realFloatFive = 1.5e-10f; // 0.00000000015f;
        System.out.println("realFloatFive = " + realFloatFive);
    }
}
